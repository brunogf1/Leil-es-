    private void listarProdutos(){
        
        String filtro = "vendido";
        ProdutosDAO dao = new ProdutosDAO();
       Connection conn = dao.connectDB();
        if (conn == null) {
            JOptionPane.showMessageDialog(null, "Erro de conex√£o");
        } else {

            List<ProdutosDTO> listaProdutos = dao.listarProdutosVendidos(filtro);

            DefaultTableModel tabelaProdutos = (DefaultTableModel) TbVendas.getModel();
            TbVendas.setRowSorter(new TableRowSorter(tabelaProdutos));
            tabelaProdutos.setNumRows(0);

            for (ProdutosDTO p : listaProdutos) {
                Object[] obj = new Object[]{
                 p.getId(),
                 p.getNome(),
                 p.getValor(),
                 p.getStatus()
                };
                tabelaProdutos.addRow(obj);
            }
            dao.desconectar();
        }
    }
    
    
